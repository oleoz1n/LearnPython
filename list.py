a = ["a", "b", "c", "d", "e", "f", "g", "h", "i", "j"]

print(a[0:])  # ['a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'i', 'j']
print(a[:0])  # []
print(a[:1])  # ['a']
print(a[-1:])  # ['j']
print(a[:-1])  # ['a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'i']
print(a[2:-4])  # ['c', 'd', 'e', 'f']
print(a[-6:-2])  # ['e', 'f', 'g', 'h']
